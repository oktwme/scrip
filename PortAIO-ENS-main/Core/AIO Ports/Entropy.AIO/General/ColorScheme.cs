namespace Entropy.AIO.General
{
    static class ColorScheme
    {
        public static string Purple(string str)
        {
            return $"<font color = \"#a32bff\">{str}</font>";
        }

        public static string Red(string str)
        {
            return $"<font color = \"#ff2a2a\">{str}</font>";
        }

        public static string Blue(string str)
        {
            return $"<font color = \"#292dff\">{str}</font>";
        }

        public static string Green(string str)
        {
            return $"<font color = \"#28ff49\">{str}</font>";
        }

        public static string Yellow(string str)
        {
            return $"<font color = \"#fffb28\">{str}</font>";
        }
    }
}